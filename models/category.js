const {DataTypes} = require('sequelize');
const sequelize = require('../configs/database');

const Category = sequelize.define('Category', {
    id: {
        type: DataTypes.INTEGER,
        autoIncrement: true,
        primaryKey: true,
    },
    name: {
        type: DataTypes.STRING(100),
        allowNull: false,
        unique: true,
    },
}, {
    timestamps: true,
    tableName: 'category',
});

module.exports = Category;